package com.xguokr.bean;

public class UserInfo {

	private String nickname ;
	private String date_created;
	private String avatar;
	private String blogs_count;
	private String questions_count;
	private String posts_count;
	private String answers_count;
	private String followings_count;
	private String introduction;
	
	public String getIntroduction() {
		return introduction;
	}
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}
	public String getFollowings_count() {
		return followings_count;
	}
	public void setFollowings_count(String followings_count) {
		this.followings_count = followings_count;
	}
	public String getAnswers_count() {
		return answers_count;
	}
	public void setAnswers_count(String answers_count) {
		this.answers_count = answers_count;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getDate_created() {
		return date_created;
	}
	public void setDate_created(String date_created) {
		this.date_created = date_created;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	public String getBlogs_count() {
		return blogs_count;
	}
	public void setBlogs_count(String blogs_count) {
		this.blogs_count = blogs_count;
	}
	public String getQuestions_count() {
		return questions_count;
	}
	public void setQuestions_count(String questions_count) {
		this.questions_count = questions_count;
	}
	public String getPosts_count() {
		return posts_count;
	}
	public void setPosts_count(String posts_count) {
		this.posts_count = posts_count;
	}
	
}
